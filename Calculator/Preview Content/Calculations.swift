import Foundation

struct Calculations {
    
    func addition(val1: Int, val2: Int) -> Double {
        return Double(val1) + Double(val2);
        }
    
    func substract(val1: Int, val2: Int) -> Double {
        return Double(val1) + Double(val2);
    }
    
    func multiply(val1: Int, val2: Int) -> Double {
        return Double(val1) * Double(val2)
    }
    
}
